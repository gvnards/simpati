<script>
export default {
  name: 'App',
  methods: {
    userIdle () {
      let idleTime = 0
      setInterval(() => {
        idleTime = idleTime + 1
        if (idleTime > 14) {
          this.$session.clear()
          this.$session.destroy()
          location.reload()
        }
      }, 60000)
      document.addEventListener('keypress', (ev) => {
        idleTime = 0
      })
      document.addEventListener('mousemove', (ev) => {
        idleTime = 0
      })
    }
  },
  created () {
    this.userIdle()
  }
}
</script>

